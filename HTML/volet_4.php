					<h1>Évaluer la couverture de la végétation aquatique pour quantifier la production de Méthylmercure (MeHg)</h1>

					<h2>Objectifs</h2>
					<p class="texteVolet">
						Plusieurs facteurs influencent la formation de méthylmercure et sa disponibilité pour les organismes aquatiques au sein de la zone des racines des végétaux aquatiques (macrophytes), notamment la teneur en mercure total dans le milieu, l’espèce de macrophyte considérée et les conditions physico-chimiques locales (turbidité, concentration en oxygène, lumière…). Une teneur élevée en oxygène et un potentiel redox élevé sont reconnus comme des facteurs limitant la méthylation. Il se peut donc que, même en présence de microorganismes capables de méthyler le Hg dans le périphyton présent sur les racines des macrophytes, les conditions environnementales ne soient pas favorables à l’activation de la méthylation du Hg (ou que la source principale de Hg ne soit pas suffisante).
          			</p>

          			<p class="texteVolet">
          				Ce volet permet de quantifier la biomasse des plantes aquatiques et du biofilm qui les recouvre au niveau de chaque lac pour évaluer la quantité potentielle de méthylmercure produite et peut-être permettre d’expliquer les différences de présence du mercure observées entre les 4 lacs. Les mesures sont effectuées durant la période de végétation (c’est-à-dire de juin à septembre).
          			</p>

          			<p class="texteVolet">
          				Dans le cadre du projet, deux communautés de macrophytes sont prises en compte : les hélophytes (macrophytes enracinées à la tige émergée) et les hydrophytes (macrophytes enracinées totalement immergées), en tant que communautés végétales structurant des zones rivulaires et littorales des lacs peu profonds. Parmi les hélophytes présentes à proximité des berges des lacs, les herbiers de Phragmites australis et Schoenoplectus pungens sont étudiés. Les nombreuses tiges immergées des roselières présentes sur une part importante du linéaire de rives des lacs, sont fréquemment recouvertes de biofilm. Plus au large, les herbiers des hydrophytes Egeria densa et Lagarosiphon major sont pris en compte : ces espèces ont la capacité de coloniser rapidement certains secteurs des lacs dès que les conditions du milieu leur sont favorables et ceci jusqu’à 7 m de profondeur parfois (lac de Cazaux-Sanguinet). De plus, ces deux espèces sont capables de modifier d’une façon importante l’oxygénation de la colonne d’eau.
          			</p>

