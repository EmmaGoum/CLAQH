						<p>
							Le rôle de l’équipe CARMA d’Irstea de Bordeaux est d’intégrer au mieux le maillon végétal aux recherches initiées dans les différents volets du projet CLAQH présentés précédemment. L’objectif est de participer à la caractérisation de la biomasse végétale et de l’activité du biofilm à l’aide de prélèvements ciblés sur différents organes de plusieurs types pertinents de plantes aquatiques qui colonisent les lacs. Ces plantes feront également l’objet d’évaluation de leur recouvrement à l’échelle des lacs. Enfin, la présence ou l’absence de conditions anoxiques, généralement nécessaires à la méthylation du Hg, pourra être vérifiée sur le terrain grâce à l’utilisation d’optodes capables de mesurer les concentrations en oxygène à différents niveaux des plantes aquatiques et de la colonne d’eau.
						</p>

						<p>
							Dans chacun des 4 lacs considérés dans le projet CLAQH, les actions suivantes sont mises en place :
							<ul>
								<li>
									Prélèvements de plantes pour mesurer la surface recouverte par le biofilm. Les plantes sont prélevées dans trois stations sur chaque lac. Leurs appareils végétatifs sont séparés (feuilles, tiges, racines, rhizomes) puis transférés aux collègues microbiologistes du projet CLAQH (volet 2) pour des essais de microbiologie (mesures de la surface recouverte par le biofilm ; mesures de la quantité de biofilm ; mesures de l’activité du biofilm). Les prélèvements sont effectués durant la période de végétation (juin à septembre).
								</li>
								<li>
									Mesures des recouvrements des deux communautés végétales (hydrophytes et hélophytes) à l’échelle des lacs. Dans le cas des hydrophytes, cette action prévoit des enregistrements effectués à l’aide d’un échosondeur couplé à un GPS. A partir de chaque enregistrement, il est possible de vérifier la présence/absence des plantes, leur recouvrement sur une surface déterminée et leur densité (abondance, hauteur, etc.). Des vérifications ponctuelles effectuées à l’aide d’un râteau permettent de valider la taxonomie des espèces détectées par l’échosondeur ; en outre, des prélèvements de plantes pour des mesures de biomasse (poids secs) sont effectués aux différentes profondeurs. Les recouvrements des hélophytes sont estimés à l’aide de photos aériennes et au logiciel ArchViewGIS ; des observations complémentaires sur le terrain seront toutefois nécessaires pour vérifier la présence/absence des plantes et la densité des tiges. Cette action permet de réaliser une estimation de la surface des lacs colonisée par les deux communautés considérées, de manière à déduire des surfaces du biofilm qui les recouvre. Les mesures sont effectuées durant la période de végétation (juin à septembre).
								</li>
