		<h1> Historique </h1>
		
		<p>
			Le projet prend ses origines au début des années 2010. Tout d’abord, à <B>l’automne 2011</B>, 25 kilos de poissons sont prélevés dans le lac d’Hourtin-Carcans, sous l’impulsion de l’association de Pêche de Hourtin, et confiés au Bureau Veritas pour des analyses. Il s’avère que les quantités de mercure retrouvées dans les poissons sont supérieures aux normes et aux recommandations sanitaires alimentaires de l’Union Européenne. <br/>
			En <B>2012</B>, les sandres pêchés dans le lac d’Hourtin sont alors interdits à la consommation. En <B>mars 2012</B> est mise en place une étude complémentaire sur les lacs d’Aquitaine. L’Agence Régionale de la Santé saisit l’ANSES (Agence nationale de sécurité sanitaire de l'alimentation, de l'environnement et du travail) pour cette étude. La fédération de pêche de Gironde et les AAPPMA (les Associations Agréées de Pêche et de Protection du Milieu Aquatique en France) effectuent des campagnes de pêche. Les échantillons de poissons s’étalent sur neuf mois, de <B>juin 2012 à février 2013</B>, et sont contrôlés par les DDTM (Direction Départementale des Territoires et de la Mer) des Landes et de Gironde. <br/>
			A la suite de cette étude, l’ANSES rend un avis en <B>juin 2013</B> relatif à la présence de mercure (Hg) dans des poissons des lacs médocains (Hourtin-Carcans et Lacanau) et landais (Cazau-Sanguinet, Parentis-Biscarrosse, Aureilhan). <br/>
			En <B>juillet 2013</B>, de nouvelles mesures de gestion sanitaires sont prises. Les sandres du lac de Lacanau sont interdits à la consommation. <br/>
			En <B>2014</B>, le projet CLAQH est mis en place pour une durée de 4 ans.
		</p>

